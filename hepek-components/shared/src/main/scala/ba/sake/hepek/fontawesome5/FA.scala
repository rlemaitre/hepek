final class FA5Icon private (
    name: String,
    tpe: FA5Icon.Type,
    size: Option[FA5Icon.Size],
    animation: Option[FA5Icon.Animation],
    flip: Option[FA5Icon.Flip],
    rotate: Option[FA5Icon.Rotate]
) {
  def regular = withTpe(FA5Icon.Type.Regular)
  def solid   = withTpe(FA5Icon.Type.Solid)

  def spinning = withAnimation(FA5Icon.Animation.Spin)
  def pulsing  = withAnimation(FA5Icon.Animation.Pulse)

  def apply(): Frag = {
    val allClasses: List[String] = List(s"fa-$name") ++ tpe.classes ++
      size.toList.flatMap(_.classes) ++
      animation.toList.flatMap(_.classes) ++
      flip.toList.flatMap(_.classes) ++
      rotate.toList.flatMap(_.classes)
    i(allClasses.map(cls := _))()
  }

  def withName(name: String): FA5Icon = copy(name = name)

  def withTpe(tpe: FA5Icon.Type): FA5Icon = copy(tpe = tpe)

  def withSize(size: Option[FA5Icon.Size]): FA5Icon = copy(size = size)

  def withSize(size: FA5Icon.Size): FA5Icon = withSize(Some(size))

  def withAnimation(animation: Option[FA5Icon.Animation]): FA5Icon = copy(animation = animation)

  def withAnimation(animation: FA5Icon.Animation): FA5Icon = withAnimation(Some(animation))

  def withFlip(flip: Option[FA5Icon.Flip]): FA5Icon = copy(flip = flip)

  def withFlip(flip: FA5Icon.Flip): FA5Icon = withFlip(Some(flip))

  def withRotate(rotate: Option[FA5Icon.Rotate]): FA5Icon = copy(rotate = rotate)

  def withRotate(rotate: FA5Icon.Rotate): FA5Icon = withRotate(Some(rotate))

  private def copy(
      name: String = name,
      tpe: FA5Icon.Type = tpe,
      size: Option[FA5Icon.Size] = size,
      animation: Option[FA5Icon.Animation] = animation,
      flip: Option[FA5Icon.Flip] = flip,
      rotate: Option[FA5Icon.Rotate] = rotate
  ) =
    new FA5Icon(name, tpe, size, animation, flip, rotate)
}

object FA5Icon {

  def apply(name: String, tpe: FA5Icon.Type): FA5Icon = new FA5Icon(
    name,
    tpe,
    size = None,
    animation = None,
    flip = None,
    rotate = None
  )

  enum Type(val classes: List[String]):
    // free
    case Solid extends Type(List("fas"))
    case Brand extends Type(List("fab"))
    // premium
    case Regular extends Type(List("far"))

  enum Size(val classes: List[String]):
    case Xs      extends Size(List("fa-xs"))
    case Sm      extends Size(List("fa-sm"))
    case lg      extends Size(List("fa-lg"))
    case Times2  extends Size(List("fa-2x"))
    case Times5  extends Size(List("fa-5x"))
    case Times10 extends Size(List("fa-10x"))

  enum Animation(val classes: List[String]):
    case Spin  extends Animation(List("fa-spin"))
    case Pulse extends Animation(List("fa-pulse"))

  enum Flip(val classes: List[String]):
    case Horizontal extends Flip(List("fa-flip-horizontal"))
    case Vertical   extends Flip(List("fa-flip-vertical"))

  enum Rotate(val classes: List[String]):
    case Rot90  extends Rotate(List("fa-rotate-90"))
    case Rot180 extends Rotate(List("fa-rotate-180"))
    case Rot270 extends Rotate(List("fa-rotate-270"))

}
